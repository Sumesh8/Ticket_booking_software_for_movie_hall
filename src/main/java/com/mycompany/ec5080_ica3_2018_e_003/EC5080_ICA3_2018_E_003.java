    //This methode run When the 'Book Now' button pressed,
    private void bookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookBtnActionPerformed
        //For set red background for conformed booking seats,
        this.selectLbl.forEach(tempLbl ->{
            tempLbl.setBackground(new Color(204,0,0));
        });
        //For open dialog box When all the seats are booked and closed 'Book Now' button,
        if(this.selectLbl.size()==84){
            JOptionPane.showMessageDialog(rootPane, 
                    "All seats are booked.", 
                    "Can't bokking seats", JOptionPane.ERROR_MESSAGE);
            this.bookBtn.setEnabled(false);
        }
        //For set total Zero and set user interfase's total zero,
        total = 0;
        totalLbl.setText(Integer.toString(total));
        //For set number of tikets Zero and set user interfase's number of tickets zero,
        numOfTickets = 0;
        ticketsLbl.setText(Integer.toString(numOfTickets));
    }//GEN-LAST:event_bookBtnActionPerformed

    //This methode run when user selected any seats or unselected any selected seats,
    private void mouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseClicked
        /**
         * @param clickLbl : For store selected label by user.
          */

        //For identify any selected seats and get into clickLabel,
        JLabel clickLbl = (JLabel)evt.getSource();
        //if clicked label(seats) is grey,
        if(clickLbl.getBackground().equals(new Color(153,153,153))){
            //To set colour as green,
            clickLbl.setBackground(new Color(51,204,0));
            //To add that label in to array,
            this.selectLbl.add(clickLbl);
            //To set seats detail in user inteface,
            seatsLbl.setText(clickLbl.getName());
            //incresed total value by 12 for all seats,
            this.total += 12;
            //To identify 10th row seats,
            if(clickLbl.getName().startsWith("Row10")){
                //Add extra 8 for the total for 10th row seats,
                this.total += 8;
            }
            //To increased number of tickets by one,
            this.numOfTickets++;
        }
        //if clicked label(seats) is green,
        else if(clickLbl.getBackground().equals(new Color(51,204,0))){
            //To set colour as grey,
            clickLbl.setBackground(new Color(153,153,153));
            //To remove that label in the array,
            this.selectLbl.remove(clickLbl);
            //To set seats detail in user inteface,
            seatsLbl.setText(clickLbl.getName());
            //decresed total value by 12 for all seats,
            this.total -= 12;
            //To identify 10th row seats,
            if(clickLbl.getName().startsWith("Row10")){
                //decresed total value by another 8 for 10th row seats,
                this.total -= 8;
            }
            //To decreased number of tickets by one,
            this.numOfTickets--;
        }
        //To set total value and number of tickets in user interface,
        this.totalLbl.setText(Integer.toString(total));
        this.ticketsLbl.setText(Integer.toString(numOfTickets));
    }//GEN-LAST:event_mouseClicked

